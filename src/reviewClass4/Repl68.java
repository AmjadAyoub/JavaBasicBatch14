package reviewClass4;

public class Repl68 {

	public static void main(String[] args) {
		/*
		 * Write a program to print out the pattern: $$$$ $ $ $ $ $$$$
		 */
		for (int j = 1; j <= 2; j++) {
			System.out.println("$$$$");
			if (j == 1) {
				System.out.println("$  $");
				System.out.println("$  $");
			}
		}

	}
}
